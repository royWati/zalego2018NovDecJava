public class Iterations {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        /* we will use a forloop
            a for loop has the startpoint,endpoint and 
            number of iterations stated as parameters
            before block of code executes. 
        
            for(startpoint ; endpoint ; iterations){
                # block of code to be repeated;
            }
        
            the varible i is used to monitor where in the 
            iteration has the code reached, thats is why
            we have it stated inside the start point.
        
          */
        for(int i=0;i<6;i++){
            System.out.println("yes");
            System.out.println(i);
        }
        
    }
    
}
